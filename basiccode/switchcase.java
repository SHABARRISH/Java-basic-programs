public class switchcase {
    public static void main(String args[]){
        int n=3;

        switch(n){
            case 1:
            System.out.print("monday");
            break;
            case 2:
            System.out.println("tues");
            break;
            case 3:
            System.out.println("weds");
            break;
            default:
            System.out.println("enter valid number");

        }
           
    }    
}
